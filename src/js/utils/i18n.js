export function getIntlLocale(locale = 'es') {
  const lc = normalizeLocale(locale);
  if (lc === 'en') return 'en-US';
  if (lc === 'pt') return 'pt-BR';
  return 'es-ES';
}

function getPathValue(obj, keyPath = '') {
  return String(keyPath || '')
    .split('.')
    .reduce((acc, part) => (acc && typeof acc === 'object' ? acc[part] : undefined), obj);
}

function interpolate(template = '', vars = {}) {
  return String(template).replace(/\{\{\s*([\w.]+)\s*\}\}/g, (_m, key) => {
    const v = getPathValue(vars, key);
    return v === undefined || v === null ? '' : String(v);
  });
}

export function t(locale = 'es', key = '', vars = {}) {
  const lc = normalizeLocale(locale);
  const raw = getPathValue(UI_TEXT[lc], key) ?? getPathValue(UI_TEXT.es, key) ?? key;
  return interpolate(raw, vars);
}

export function applyDocumentI18n(doc = document, locale = 'es') {
  if (!doc) return;
  const lang = normalizeLocale(locale);
  doc.documentElement.lang = lang;

  doc.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(lang, key);
  });

  doc.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.setAttribute('placeholder', t(lang, key));
  });

  doc.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    el.setAttribute('title', t(lang, key));
  });

  doc.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria-label');
    el.setAttribute('aria-label', t(lang, key));
  });
}
